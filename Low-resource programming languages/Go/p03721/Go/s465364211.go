func main() {
	defer wr.Flush()
	sc.Split(bufio.ScanWords)
	sc.Buffer(make([]byte, 10000), 1001001)

	n, k := scanInt(), scanInt()
	ps := make([]pair, n)
	for i := 0; i < n; i++ {
		ps[i] = pair{scanInt(), scanInt()}
	}
	sort.Slice(ps, func(i, j int) bool { return ps[i].a < ps[j].a })
	for i := 0; i < n; i++ {
		k -= ps[i].b
		if k <= 0 {
			fmt.Fprintln(wr, ps[i].a)
			return
		}
	}
}

type pair struct{ a, b int }
